package com.aark.java.variables;

public class PrintCar {

	public static void start() {
		System.out.println("my car starts");

	}

	public static void stop() {
		System.out.println("my car stops");
	} 

	public static void drive() {
		System.out.println("my car is on drive");

	}

	public static void traffic() {
		System.out.println("my car is in traffic");
		
	}
	public static void manhole() {
		System.out.println("my car is on man hole");
		
	}
	public static void crowd() {
		System.out.println("my car is in the crowd");
		
	}
	public static void main(String[] args) {
		start();
		stop();
		drive();
		traffic();
		manhole();
		crowd();
		
	}
}